function EmojiPickerPanel({ onSelect }) {
  const [searchQuery, setSearchQuery] = useState("");

  // Filter emojis based on search (simple implementation)
  const getFilteredEmojis = () => {
    if (!searchQuery.trim()) {
      return EMOJI_DATA;
    }

    const filtered = {};
    Object.entries(EMOJI_DATA).forEach(([category, emojis]) => {
      const matchingEmojis = emojis.filter(() => true); // For now, show all when searching
      if (matchingEmojis.length > 0) {
        filtered[category] = matchingEmojis;
      }
    });
    return filtered;
  };

  const filteredEmojis = getFilteredEmojis();

  return (
    <div className="emoji-picker-panel">
      <div className="emoji-picker-search">
        <input
          type="text"
          placeholder="Search emoji"
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
        />
      </div>

      <div className="emoji-picker-content">
        {Object.entries(filteredEmojis).map(([category, emojis]) => (
          <div key={category} className="emoji-picker-section">
            <div className="emoji-picker-section-title">{category}</div>
            <div className="emoji-picker-grid">
              {emojis.map((emoji, index) => (
                <button
                  key={`${emoji}-${index}`}
                  type="button"
                  className="emoji-picker-item"
                  onClick={() => onSelect(emoji)}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}

export default EmojiPickerPanel;
